                                <p class="SubTitulo" id="Topico3">Conceito de Balanced Scorecard</p> 
                                
                                <p class="Texto"><strong>É preciso compreender que o BSC é uma ferramenta de acompanhamento e integração, entre o presente e o passado, utilizando sempre dados de todas as esferas da empresa. </strong></p>
                                
                                <p class="Texto">De acordo com Kaplan e Norton (1997), o BSC é um novo instrumento que integra as medidas derivadas da estratégia. Sem menosprezar as medidas financeiras do desempenho passado, incorpora os vetores do desempenho financeiro futuro. Esses vetores, que abrangem as perspectivas do cliente, dos processos internos, e do aprendizado e crescimento, nascem de um esforço consciente e rigoroso de tradução da estratégia organizacional em objetivos e medidas tangíveis. </p>
                                
                                <p class="Texto">Segundo Kaplan e Norton (1997), o Balanced Scorecard complementa as medidas financeiras do desempenho passado com medidas dos vetores que impulsionam o desempenho futuro. Os objetivos e medidas do Scorecard derivam da visão e estratégia da empresa. Os objetivos e medidas focalizam o desempenho organizacional sob quatro perspectivas: financeiras, do cliente, dos processos internos e de aprendizado e crescimento. Essas perspectivas formam a estrutura do Balanced Scorecard.</p>
                                
                                <p class="Texto">De acordo com Padoveze (2003), o Balanced Scorecard é um sistema de informação para gerenciamento da estratégia empresarial. Traduz a missão e a estratégia da empresa em um conjunto abrangente de medidas de desempenho financeiras e não-financeiras que serve de base para um sistema de medição e gestão estratégica.</p>

                                <p class="Texto">Para Neves (2005), o BSC é um sistema gerencial que deixa claro que medidas financeiras e não-financeiras devem fazer parte do sistema de informações das organizações. Deve traduzir a missão e a estratégia de uma unidade de negócios em objetivos e medidas tangíveis. </p>                   
                        
                                <p class="Texto">Essas medidas representam o equilíbrio entre indicadores externos voltados para acionistas e clientes, e as medidas internas dos processos críticos de negócios, inovação, aprendizado e crescimento, ou seja, o BSC é um sistema gerencial utilizado pelas organizações para administrar a gestão estratégica de longo prazo, de forma a viabilizar processos gerenciais críticos.</p>

                                <p class="Texto">Conforme Kaplan e Norton (1997, p. 8):</p>

                                <div class="col-md-12">
                                    <blockquote class="blockquoteEad">
                                        <p class="mb-0">O Balanced Scorecard leva o conjunto de objetivos das unidades de negócios além das medidas financeiras sumarizadas. Os executivos podem agora avaliar até que ponto suas unidades de negócios geram valor para os clientes atuais e futuros, e como devem aperfeiçoar as capacidades internas e os investimentos necessários em pessoal, sistemas e procedimentos visando a melhorar o desempenho futuro. O Balanced Scorecard capta as atividades críticas de geração do valor criadas por funcionários e executivos capazes e motivados da empresa. Preservando o interesse no desempenho de curto prazo, através da perspectiva financeira, o Balanced Scorecard revela claramente os vetores de valor para um desempenho financeiro e competitivo superior a longo prazo.</p>
                                        <footer class="blockquote-footer">(KAPLAN; NORTON, 1997, p. 8)</footer>
                                    </blockquote>
                                </div>

                                <p class="Texto"><strong>O BSC é, para os executivos, uma ferramenta completa que traduz a visão e a estratégia da empresa num conjunto coerente de medidas de desempenho. Muitas empresas adotaram declarações de missão para transmitir valores e crenças fundamentais a todos os funcionários. A declaração de missão aponta as crenças essenciais, e identifica os mercados-alvo e produtos essenciais.</strong></p>

                                <p class="Texto">Os executivos têm que compreender que o Balanced Scorecard serve como instrumento de informação para auxiliar a tomada de decisão, mas não vai resolver todos os problemas da organização. De acordo com Kaplan e Norton (1997, p. 9):</p>

                                <div class="col-md-12">
                                    <blockquote class="blockquoteEad">
                                        <p class="mb-0">O Balanced Scorecard deixa claro que as medidas financeiras e não-financeiras devem fazer parte do sistema de informações para funcionários de todos os níveis da organização. Os funcionários da linha de frente precisam compreender as conseqüências financeiras de suas decisões e ações; os altos executivos precisam reconhecer os vetores do sucesso a longo prazo. Os objetivos e as medidas utilizadas no Balanced Scorecard não se limitam a um conjunto aleatório de medidas de desempenho financeiro e não-financeiro, pois derivam de um processo hierárquico norteado pela missão e pela estratégia da unidade de negócios. O Balanced Scorecard deve traduzir a missão e a estratégia de uma unidade de negócios em objetivos e medidas tangíveis. As medidas representam o equilíbrio entre indicadores externos voltados para acionistas e clientes, e as medidas internas de processos críticos de negócios, inovação, aprendizado e crescimento. Há um equilíbrio entre as medidas de resultado – as conseqüências dos esforços do passado – e as medidas que determinam o desempenho futuro. E o Scorecard se equilibra entre medidas objetivas, de resultado, facilmente quantificáveis, e vetores subjetivos, até certo ponto discricionário, das medidas de resultado.</p>
                                        <footer class="blockquote-footer">(KAPLAN; NORTON, 1997, p. 9)</footer>
                                    </blockquote>
                                </div>

                                <p class="Texto">As empresas adotaram a filosofia do Scorecard para viabilizar processos gerenciais críticos: esclarecer e traduzir a visão e a estratégia, comunicar e associar objetivos e medidas estratégicas, planejar, estabelecer metas e alinhar iniciativas estratégicas e melhorar o feedback e o aprendizado estratégico. Com a adoção do BSC as empresas pretendem alcançar o desempenho empresarial. </p>

                                <p class="Texto">Segundo Kaplan e Norton (1997), o verdadeiro poder do Balanced Scorecard, todavia, ocorre quando deixa de ser um sistema de medidas e se transforma em um sistema de gestão estratégica. Conforme cresce a adoção do Balanced Scorecard, as empresas constatam que pode ser utilizado para:</p>

                                <p class="Texto">
                                a)	Esclarecer e obter consenso em relação à estratégia;
                                </p>

                                <p class="Texto">
                                b)	Comunicar a estratégia a toda a empresa;
                                </p>

                                <p class="Texto">
                                c)	Alinhar as metas departamentais e pessoais à estratégia;
                                </p>

                                <p class="Texto">
                                d)	Associar os objetivos estratégicos com metas de longo prazo e orçamentos anuais;
                                </p>

                                <p class="Texto">
                                e)	Identificar e alinhar as iniciativas estratégicas;
                                </p>

                                <p class="Texto">
                                f)	Realizar revisões estratégicas periódicas e sistemáticas;
                                </p>

                                <p class="Texto">
                                g)	Obter feedback para aprofundar o conhecimento da estratégia e aperfeiçoá-la.
                                </p>

                                <p class="Texto">
                                O BSC preenche a lacuna existente na maioria dos sistemas gerenciais – a falta de um processo sistemático para implementar e obter feedback sobre a estratégia. Os processos gerenciais construídos a partir do Scorecard asseguram que a organização fique alinhada e focalizada na implementação da estratégia de longo prazo. Assim entendido, o BSC torna-se a base para o gerenciamento das empresas na era da informação.
                                </p>
              
