                                <h2 class="Titulo" >BALANCED SCORECARD</h2>
                                                    
                                <p class="Texto">Esta seção aborda o Balanced Scorecard, comentando sua origem, contribuição nas organizações, conceitos, bem como a maneira de utilização dessa metodologia como sistema de gestão estratégica.</p>

                                <p class="SubTitulo" id="Topico1">1.1. A origem do Balanced Scorecard</p> 
                                
                                <p class="Texto">De acordo com Neves (2005), a década de 1980 foi marcada pela competição em nível global. Rápidos avanços tecnológicos, proliferação de produtos, crescente escassez de recursos humanos e naturais, diminuição da participação das organizações no mercado e perda da qualidade. </p>
                                
                                <p class="Texto">Essa era foi dita como industrial, em que as alocações de recursos eram puramente financeiras e físicas utilizando-se indicadores financeiros e de produtividade para mensurar o desempenho das empresas, o que levou estas a se desenvolverem pela busca de competitividade e sobrevivência.</p>
                                
                                <p class="Texto">Segundo Neves (2005), as organizações entraram na era da informação, na qual os executivos necessitavam de indicadores sobre vários aspectos do ambiente e desempenho organizacional, para conseguir a excelência empresarial e a fatia de mercado.</p>
                                
                                <p class="Texto">Foi em 1990 que começaram as pesquisas sobre o BSC em diversas empresas americanas, desenvolvidas pelo instituto Nolan Norton tendo como idealizadores David Norton e Robert Kaplan. </p>

                                <p class="Texto"><strong>O objetivo dessa pesquisa era encontrar novos métodos de avaliação de desempenho empresarial. Para Kaplan e Norton (1997), o estudo foi motivado pela crença de que os métodos existentes para avaliação do desempenho empresarial, em geral apoiados nos indicadores contábeis e financeiros, estavam se tornando obsoletos. Os participantes do estudo acreditavam que depender de medidas de desempenho consolidadas, baseadas em dados financeiros, estava prejudicando a capacidade das empresas de criar valor econômico para o futuro.</strong> </p>                   
                        
                                <p class="Texto">De acordo com Neves (2005), no início dos anos 1990, em Harvard, os professores Robert S. Kaplan e David P. Norton propuseram uma nova abordagem para resolver problemas de mensuração frente aos novos desafios organizacionais das empresas inseridas na economia do conhecimento.</p>

                                <p class="Texto">Os professores constataram que os indicadores financeiros isoladamente se mostravam incapazes de medir as atividades criadoras de valor relacionadas com os ativos intangíveis como habilidades, competências e motivação dos empregados, bancos de dados e tecnologias da informação, processos, inovação em produtos e serviços, relacionamentos com os clientes, imagem da organização e outras. Surge então o BSC.</p>

                                <p class="Texto">As primeiras pesquisas nas empresas mostraram que existiam várias medidas de desempenho além das financeiras. De acordo com Kaplan e Norton (1997), o estudo mostrava também como estava utilizando um recém-criado Balanced Scorecard corporativo que continha, além de várias medidas financeiras tradicionais, outras medidas de desempenho relativas a prazos de entrega de clientes, qualidade e ciclo de processos de produção, e também eficácia no desenvolvimento de novos produtos.</p>

                                <p class="Texto">Conforme Neves (2005), o primeiro passo foi à busca da qualidade, assim as empresas buscavam competitividade em gestão de aspectos não financeiros. Assim surgiram indicadores de desempenho de níveis de qualidade com prazos de respostas, índices de defeitos, compromissos de entregas, entre outros.</p>

                                <p class="Texto">Segundo Kaplan e Norton (1997), várias outras ideias foram apresentadas durante a primeira fase do estudo, entre as quais: valor para o acionista, medidas de produtividade e qualidade, e novos planos de compensação, mas os participantes logo se voltaram para o Balanced Scorecard multidimensional como a ferramenta mais promissora.</p>

                                <p class="Texto">A denominação, Balanced Scorecard, refletia o equilíbrio entre objetivos de curto e longo prazos, entre medidas financeiras e não-financeiras, entre indicadores de tendências e ocorrências e entre as perspectivas interna e externa de desempenho.</p>

                                <p class="Texto">Devido ao surgimento de várias ideias surgidas sobre modelos de avaliação de desempenho em diversos setores da empresa, que de acordo com Kaplan e Norton (1997), levaram a uma ampliação do Scorecard, que se transformou no que se chama de Balanced Scorecard, organizado em torno de quatro perspectivas distintas – financeira, do cliente, interna e de inovação e aprendizado.</p>

                                <p class="Texto">De acordo com Kaplan e Norton (1997, p. 8):</p>
                                
                                <div class="col-md-12">
                                    <blockquote class="blockquoteEad">
                                        <p class="mb-0">Reforçou a importância da vinculação das medidas do Balanced Scorecard à estratégia organizacional. Embora aparentemente óbvio, a verdade é que a maioria das empresas, mesmo aquelas que estavam implementando novos sistemas de medida de desempenho, não alinhava as medidas às suas estratégias. A maioria procurava melhorar o resultado dos processos existentes – através de custos mais baixos, melhor qualidade e tempos menores de resposta – mas não sabia identificar os processos realmente estratégicos: aqueles que devem apresentar um desempenho excepcional para que a estratégia da empresa seja bem-sucedida.</p>
                                        <footer class="blockquote-footer">(KAPLAN; NORTON, 1997, p. 8)</footer>
                                    </blockquote>
                                </div>

                                <p class="Texto">Portanto, em vez de obrigar a escolhas complexas, as medidas diversificadas tinham uma relação direta com a estratégia e se interligavam por uma série de relações de causa e efeito. Juntas, descreviam a trajetória estratégica – mostrando como investimentos na reciclagem de funcionários, tecnologia da informação e produtos e serviços inovadores melhorariam radicalmente o desempenho financeiro futuro.</p>

                                <p class="Texto">Para Kaplan e Norton (1997), os altos executivos dessas empresas utilizavam o Balanced Scorecard como a principal ferramenta organizacional para importantes processos gerenciais: estabelecimento de metas individuais e de equipe, remuneração, alocação de recursos, planejamento, orçamento, feedback e aprendizado estratégicos.</p>

                                <p class="Texto"><strong>Com todos esses estudos feitos nas grandes organizações se revelou que os executivos estavam utilizando o Balanced Scorecard não só como medida de desempenho, mas também como sistema de acompanhamento das estratégias e segundo Kaplan e Norton (1997), os executivos arrojados utilizavam o Balanced Scorecard não apenas para esclarecer e comunicar a estratégia, mas também para gerenciá-la.</strong></p>
              
